package com.zpj.shouji.market.bean;

import android.graphics.drawable.Drawable;

public abstract class BaseAppInfo {

    private String packageName;

    private String appName;

    private String versionName;

    private String versionCode;

    private String appSize;

    private Drawable iconDrawable;

    private String appType;

    public String getPackageName() {
        return packageName;
    }

    public void setPackageName(String packageName) {
        this.packageName = packageName;
    }

    public String getAppName() {
        return appName;
    }

    public void setAppName(String appName) {
        this.appName = appName;
    }

    public String getVersionName() {
        return versionName;
    }

    public void setVersionName(String versionName) {
        this.versionName = versionName;
    }

    public String getVersionCode() {
        return versionCode;
    }

    public void setVersionCode(String versionCode) {
        this.versionCode = versionCode;
    }

    public String getAppSize() {
        return appSize;
    }

    public void setAppSize(String appSize) {
        this.appSize = appSize;
    }

    public Drawable getIconDrawable() {
        return iconDrawable;
    }

    public void setIconDrawable(Drawable iconDrawable) {
        this.iconDrawable = iconDrawable;
    }

    public String getAppType() {
        return appType;
    }

    public void setAppType(String appType) {
        this.appType = appType;
    }

    public abstract boolean isInstalledApp();

    public abstract boolean isApkPackage();

    public abstract boolean isXpkPackage();

    public abstract boolean isMarketApp();

}
