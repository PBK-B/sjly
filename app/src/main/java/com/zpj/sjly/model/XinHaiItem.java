package com.zpj.sjly.model;

public class XinHaiItem {

    private String appSite;
    private String appImgSite;
    private String appTitle;
    private String appDescription;
    private String appInfo;

    public String getAppSite() {
        return appSite;
    }

    public void setAppSite(String appSite) {
        this.appSite = appSite;
    }

    public String getAppImgSite() {
        return appImgSite;
    }

    public void setAppImgSite(String appImgSite) {
        this.appImgSite = appImgSite;
    }

    public String getAppTitle() {
        return appTitle;
    }

    public void setAppTitle(String appTitle) {
        this.appTitle = appTitle;
    }

    public String getAppDescription() {
        return appDescription;
    }

    public void setAppDescription(String appDescription) {
        this.appDescription = appDescription;
    }

    public String getAppInfo() {
        return appInfo;
    }

    public void setAppInfo(String appInfo) {
        this.appInfo = appInfo;
    }
}
